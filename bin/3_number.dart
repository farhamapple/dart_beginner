void main() {
  int number1 = 10;
  double number2 = 10.5;

  print(number1);
  print(number2);

  // tipe data num
  // jika kit ingin menggunakan tipe data number yang bisa int ataupun doble, kita bisa menggunakna tipe data enum;
  num numberNum = 10;
  print(numberNum);

  numberNum = 15;
  print(numberNum);

  // Tipe dat aBoolean
  bool finish = false;
  print(finish);

  finish = true;
  print(finish);
}
